public class Comprador {
    private String cpf;
    private String nome;
    private int telefone;
    private Endereco endereco;

    public String getCpf() {
		return cpf;
	}
    public String getNome() {
		return nome;
	}
    public int getTelefone() {
		return telefone;
	}
    public Endereco getEndereco() {
		return endereco;
	}

}
